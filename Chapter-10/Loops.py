# Loops -- While & For Loop

# While Loop:

count = 95 
while count < 100:
    print(count)
    count = count + 1
else:
    print("While Loop Ended!")

# For Loop:

for x in range(5):
    print(x)

for y in range(1,10):
    print(y)

for z in range(-5, 6):
    print(z)    

for i in range(0,10,2):
    print(i)        