# DataTypes In Python

a = 1
b = 2
print(a+b)
print(type(a)) # Checking DataType : Integer

c = "1"
d = "1"
print(c+d)
print(type(c)) # Checking DataType : String

# Basic DataTypes In Python:

# 1. Numeric
a1 = 1 # Integer
a2 = 1.5 # Float
a3 = complex(3,5) # Complex

# 2. Sequence
b1 = "Yahya" # String
b2 = [1,44,63,67,55, "Yahya"] # List
print(type(b2))
b3 = (1,44,63,67,55, "Yahya") # Tuple
print(type(b3))

# 3. Dictionary
my_dictionary = {'name': 'Yahya', 'age': 15, 'city': 'Delhi'}
print(type(my_dictionary))

# 4. Set
my_set = {1,44,63,67,55, "Yahya"}
print(type(my_set))

# 5. Boolean
bool1 = True
print(type(bool1))
bool2 = False
print(type(bool2))

